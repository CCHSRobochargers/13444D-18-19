// for moving forward or backwords
static const float ticksPerInch = 0.967 * (627.2 / (4.0 * PI));

// for turning the Robot
static const float ticksPerDeg = 1.0 * (ticksPerInch * 14.5 * PI) / 360.0;

// for raising the lift
static const float ticksPerInchRaised = 3968.0/14.0; // * (627.2 * 0.75 * PI);

// used for LCD screen
