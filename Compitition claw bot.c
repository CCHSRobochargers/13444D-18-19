// used to drive the robot forward
void move(float dist, int speed, bool hold)		//used to drive the robot forward
{
	resetMotorEncoder(rightMotor);
	resetMotorEncoder(leftMotor);
	resetMotorEncoder(rightRear);
	resetMotorEncoder(leftRear);

	setMotorTarget(rightMotor, dist*ticksPerInch, speed, hold);
	setMotorTarget(leftMotor, dist*ticksPerInch, speed, hold);
	setMotorTarget(rightRear, dist*ticksPerInch, speed, hold);
	setMotorTarget(leftRear, dist*ticksPerInch, speed, hold);

	while (!getMotorTargetCompleted(rightMotor) && !getMotorTargetCompleted(leftMotor) && !getMotorTargetCompleted(rightRear)&& !getMotorTargetCompleted(leftRear))
		wait1Msec(10);
}

// used to turn the robot
void spin(float deg, int speed, bool hold)	//used to turn the robot
{
	resetMotorEncoder(rightMotor);
	resetMotorEncoder(leftMotor);
	resetMotorEncoder(rightRear);
	resetMotorEncoder(leftRear);

	setMotorTarget(rightMotor, -deg * ticksPerDeg, speed, hold);
	setMotorTarget(leftMotor, deg * ticksPerDeg, speed, hold);
	setMotorTarget(rightRear,  -deg * ticksPerDeg, speed, hold);
	setMotorTarget(leftRear, deg * ticksPerDeg, speed, hold);

	// due to the way the motor targets are set, a positive degree value will turn the robot clockwise, or right
	// further, a negative value turns the robot counterclockwise, or left

	while (!getMotorTargetCompleted(rightMotor) && !getMotorTargetCompleted(leftMotor) && !getMotorTargetCompleted(rightRear)&& !getMotorTargetCompleted(leftRear))
		wait1Msec(10);
}

// used to raise the lift
void raise (float inches, int speed, bool hold)
{
	resetMotorEncoder(liftMotor);

	setMotorTarget(liftMotor, (long)(inches * ticksPerInchRaised), speed, hold);
}
void raiseWait (void)
{
	while (!getMotorTargetCompleted(liftMotor))
		wait1Msec(10);
}

